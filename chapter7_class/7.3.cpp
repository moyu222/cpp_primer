#include <iostream>
#include "Sales_data_struct.hpp"
using namespace std;

int main()
{
    cout << "ISBN(bookNo), units_sold, revenue: " << endl;
    Sales_data total;
    if (read(cin, total))
    {
        Sales_data trans;
        while (read(cin, trans))
        {
            if (total.isbn() == trans.isbn())
                total.combine(trans);
            else
            {
                print(cout, total) << endl;
                total = trans;
            }
        }
        print(cout, total) << endl;
    }
    else
    {
        cerr << "No data?!" << endl;
    }
}